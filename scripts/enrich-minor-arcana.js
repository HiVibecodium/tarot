try {
  console.log('Reading cards.json...');
  const cards = JSON.parse(fs.readFileSync(cardsPath, 'utf8'));

  console.log(`Found ${cards.length} cards. Enriching Minor Arcana...`);

  const now = new Date().toISOString();
  let enrichedCount = 0;

  const enrichedCards = cards.map(card => {
    if (card.arcana === 'minor') {
      const idParts = card._id.split('-');
      const suit = idParts[0];
      const lastPart = idParts[idParts.length - 1];
      const cardKey = `${suit}-${lastPart}`;

      if (MINOR_ARCANA_FULL[cardKey]) {
        const enrichment = MINOR_ARCANA_FULL[cardKey];
        Object.assign(card, enrichment);
        card.updatedAt = now;
        enrichedCount++;
      }
    }
    return card;
  });

  console.log('Writing enriched cards...');
  fs.writeFileSync(cardsPath, JSON.stringify(enrichedCards, null, 2), 'utf8');

  console.log(`Done! Enriched ${enrichedCount} Minor Arcana cards with detailed data.`);

} catch (error) {
  console.error('Error:', error.message);
  process.exit(1);
}
