/**
 * Get dominant element interpretation
 */
export function getDominantElementInterpretation(elementBalance) {
  const elements = Object.entries(elementBalance);
  const dominant = elements.reduce((max, curr) =>
    curr[1].percentage > max[1].percentage ? curr : max
  );

  const elementName = dominant[0];
  const percentage = dominant[1].percentage;

  if (percentage >= 40) {
    return {
      element: elementName,
      interpretation: ELEMENT_DETAILED[elementName]?.highPercentage.interpretation,
      type: 'dominant'
    };
  }

  return null;
}

/**
 * Get lacking element interpretation
 */
export function getLackingElementInterpretation(elementBalance) {
  const elements = Object.entries(elementBalance);
  const lacking = elements.reduce((min, curr) =>
    curr[1].percentage < min[1].percentage ? curr : min
  );

  const elementName = lacking[0];
  const percentage = lacking[1].percentage;

  if (percentage <= 15) {
    return {
      element: elementName,
      interpretation: ELEMENT_DETAILED[elementName]?.lowPercentage.interpretation,
      type: 'lacking'
    };
  }

  return null;
}

/**
 * Get complete element balance analysis
 */
export function getElementBalanceAnalysis(elementBalance) {
  const dominant = getDominantElementInterpretation(elementBalance);
  const lacking = getLackingElementInterpretation(elementBalance);

  return {
    dominant,
    lacking,
    isBalanced: !dominant && !lacking
  };
}
