/**
 * Get interpretation for an aspect
 * @param {string} planet1 - First planet (English name)
 * @param {string} planet2 - Second planet (English name)
 * @param {string} aspectType - Type of aspect (conjunction, opposition, trine, square, sextile)
 * @returns {object|null} - Aspect interpretation object or null
 */
export function getAspectInterpretation(planet1, planet2, aspectType) {
  // Normalize planet order (alphabetical)
  const [p1, p2] = [planet1, planet2].sort()

  // Map Russian aspect names to English
  const aspectMap = {
    'Соединение': 'conjunction',
    'Оппозиция': 'opposition',
    'Трин': 'trine',
    'Квадрат': 'square',
    'Секстиль': 'sextile',
    'conjunction': 'conjunction',
    'opposition': 'opposition',
    'trine': 'trine',
    'square': 'square',
    'sextile': 'sextile'
  };

  const normalizedAspect = aspectMap[aspectType] || aspectType.toLowerCase();

  // Create key
  const key = `${p1}-${p2}-${normalizedAspect}`

  return ASPECT_INTERPRETATIONS[key] || null
}

/**
 * Check if aspect is in our library
 */
export function hasAspectInterpretation(planet1, planet2, aspectType) {
  return getAspectInterpretation(planet1, planet2, aspectType) !== null
}
