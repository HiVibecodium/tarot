async function seedCards() {
  try {
    console.log('ðŸŒ± Starting card seeding...');

    // Check if cards already exist
    const existingCount = await Card.count();

    if (existingCount > 0) {
      console.log(`âš ï¸  Database already has ${existingCount} cards`);
      const readline = require('readline').createInterface({
        input: process.stdin,
        output: process.stdout
      });

      return new Promise((resolve) => {
        readline.question('Delete and re-seed? (yes/no): ', async (answer) => {
          readline.close();

          if (answer.toLowerCase() === 'yes') {
            await Card.deleteAll();
            console.log('âœ… Existing cards deleted');
          } else {
            console.log('âŒ Seeding cancelled');
            process.exit(0);
          }

          await performSeed();
          resolve();
        });
      });
    } else {
      await performSeed();
    }

  } catch (error) {
    console.error('âŒ Seed failed:', error);
    process.exit(1);
  }
}

async function performSeed() {
  console.log(`ðŸ“ Inserting ${allCards.length} cards...`);

  await Card.insertMany(allCards);

  const finalCount = await Card.count();
  console.log(`âœ… Seeding complete! ${finalCount} cards in database`);

  // Show breakdown
  const majorCount = allCards.filter(c => c.arcana === 'major').length;
  const minorCount = allCards.filter(c => c.arcana === 'minor').length;

  console.log(`   Major Arcana: ${majorCount}`);
  console.log(`   Minor Arcana: ${minorCount}`);
  console.log('\nâœ¨ Cards are ready for readings!\n');

  process.exit(0);
}

